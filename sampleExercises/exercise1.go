package main

import (
	"fmt"
)

func main() {
	// using a short declaration code to assign values to variable
	x := 42
	y := "James Bond"
	z := true
	// printing it in single fmt statement
	fmt.Println(x, y, z)
	// printing it in multiple statements
	fmt.Println("x = ", x)
	fmt.Println("y = ", y)
	fmt.Println("z = ", z)
}
